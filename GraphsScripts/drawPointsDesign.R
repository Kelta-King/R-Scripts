# Draw line for sequence
plot(1:10, type='l')

# Draw line connecting all the points
plot(c(3,4,5), c(6,3,5), type='l')

# Plot with labels
plot(1:10, main='title', xlab='x-axis', ylab='y-axis')

# Color of graph points
plot(1:10, col='red')

# Point shape of graph
plot(1:10, pch=25)

